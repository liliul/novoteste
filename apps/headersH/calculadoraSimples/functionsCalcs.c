int mais(int num1 , int num2) {
    int somar = (num1 + num2);
    return somar;
}
int menos(int num1, int num2) {
    int somar = num1 - num2;
    return somar;
}
int multiplicar(int num1, int num2) {
    int somar = num1 * num2;
    return somar;
}
int divisao(int num1, int num2) {
    int somar = num1 / num2;
    return somar;
}
